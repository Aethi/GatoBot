# ASCII string format codes

class StrFmt:
    Reset       = '\033[0m'
    # Colors
    Cyan        = '\033[96m'
    Purple      = '\033[95m'
    Blue        = '\033[94m'
    Yellow      = '\033[93m'
    Green       = '\033[92m'
    Red         = '\033[91m'
    # Decorators
    Bold        = '\033[1m'
    Underline   = '\033[4m'